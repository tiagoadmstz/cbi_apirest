CREATE TABLE DADOS_BOBINAGEM (
    ID BIGINT NOT NULL,
    OS VARCHAR(50),
    DATA TIMESTAMP,
    CLIENTES VARCHAR(255),
    PRAZOFINALIZACAO TIMESTAMP,
    PRAZOREBOBINAMENTO TIMESTAMP,
    PRAZOMONTAGEM TIMESTAMP,
    PLACA VARCHAR(255),
    CATEGORIA VARCHAR(50),
    SERIE VARCHAR(255),
    MARCA VARCHAR(50),
    POTENCIA VARCHAR(255),
    MODELO VARCHAR(50),
    DATA_DE_FABRICACAO TIMESTAMP,
    RPM VARCHAR(255),
    TENSAO VARCHAR(100),
    CORRENTE VARCHAR(255),
    ENROLAMENTO_DE_FABRICA BOOLEAN,
    PATRIMONIO VARCHAR(255),
    FORMA_CONSTRUTIVA VARCHAR(255),
    CAIXA_DE_LIGACAO VARCHAR(255),
    ACOPLAMENTO VARCHAR(255),
    CABOS VARCHAR(255),
    FECHAMENTO VARCHAR(255),
    COMPRIMENTO VARCHAR(255),
    DIAMETRO VARCHAR(255),
    RANHURA VARCHAR(255),
    CAMADA VARCHAR(255),
    PASSO VARCHAR(255),
    ESPIRAS VARCHAR(255),
    FIO VARCHAR(255),
    SECCAO VARCHAR(50),
    PASSO_AUX VARCHAR(255),
    ESPIRA_AUX VARCHAR(255),
    FIO_AUX VARCHAR(255),
    SECCAOAUX VARCHAR(50),
    PESO VARCHAR(255),
    LIGACAO VARCHAR(255),
    INFADICIONAIS LONG VARCHAR,
    CAUSA_DA_QUEIMA VARCHAR(255),
    OBSERVACOES LONG VARCHAR,
    NOTA_FISCAL VARCHAR(255),
    FORMA VARCHAR(255),
    CALCO VARCHAR(255),
    ANEXOS BLOB,
    FECHADO BOOLEAN,
    VIBRACAO BOOLEAN,
    PALETE VARCHAR(10),
    PRIMARY KEY(ID)
);

CREATE INDEX IDX_PRAZOS ON DADOS_BOBINAGEM(PRAZOFINALIZACAO, PRAZOREBOBINAMENTO, PRAZOMONTAGEM);

CREATE SEQUENCE seq_dados_bobinagem
MINVALUE 1
START WITH 1
INCREMENT BY 1;